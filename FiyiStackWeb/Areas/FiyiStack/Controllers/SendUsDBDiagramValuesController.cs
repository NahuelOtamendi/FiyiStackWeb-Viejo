        [HttpPost("~/api/FiyiStack/SendUsDBDiagram/1/InsertOrUpdateAsync")]
        public async Task<IActionResult> InsertOrUpdateAsync()
        {
            try
            {
                #region Pass data from client to server


                string FileUploaded = "";
                //Look for sent files
                if (HttpContext.Request.Form.Files.Count != 0)
                {
                    int i = 0; //Used to iterate in HttpContext.Request.Form.Files
                    foreach (var File in Request.Form.Files)
                    {
                        if (File.Length > 0)
                        {
                            var FileName = HttpContext.Request.Form.Files[i].FileName;
                            var FilePath = $@"{_WebHostEnvironment.WebRootPath}/Uploads/FiyiStack/SendUsDBDiagram/";
                            FileUploaded = "/Uploads/FiyiStack/SendUsDBDiagram/" + FileName;

                            using (var FileStream = new FileStream($@"{FilePath}{FileName}", FileMode.Create))
                            {

                                await File.CopyToAsync(FileStream); // Read file to stream
                                byte[] array = new byte[FileStream.Length]; // Stream to byte array
                                FileStream.Seek(0, SeekOrigin.Begin);
                                FileStream.Read(array, 0, array.Length);
                            }

                            i += 1;
                        }
                    }
                }

                //SendUsDBDiagramId
                int SendUsDBDiagramId = Convert.ToInt32(HttpContext.Request.Form["fiyistack-sendusdbdiagram-sendusdbdiagramid-input"]);
                
                string Title = HttpContext.Request.Form["fiyistack-sendusdbdiagram-title-input"];
                string Description = HttpContext.Request.Form["fiyistack-sendusdbdiagram-description-input"];
                
                
                #endregion

                int NewEnteredId = 0;
                int RowsAffected = 0;

                if (SendUsDBDiagramId == 0)
                {
                    //Insert
                    SendUsDBDiagramModel SendUsDBDiagramModel = new SendUsDBDiagramModel()
                    {
                        Active = true,
                        UserCreationId = 1,
                        UserLastModificationId = 1,
                        DateTimeCreation = DateTime.Now,
                        DateTimeLastModification = DateTime.Now,
                        Title = Title,
                        Description = Description,
                        FileUploaded = FileUploaded,
                        
                    };
                    
                    NewEnteredId = _ISendUsDBDiagram.Insert(SendUsDBDiagramModel);
                }
                else
                {
                    //Update
                    SendUsDBDiagramModel SendUsDBDiagramModel = new SendUsDBDiagramModel(SendUsDBDiagramId);
                    
                    SendUsDBDiagramModel.UserLastModificationId = 1;
                    SendUsDBDiagramModel.DateTimeLastModification = DateTime.Now;
                    SendUsDBDiagramModel.Title = Title;
                    SendUsDBDiagramModel.Description = Description;
                    SendUsDBDiagramModel.FileUploaded = FileUploaded;
                                       

                    RowsAffected = _ISendUsDBDiagram.UpdateBySendUsDBDiagramId(SendUsDBDiagramModel);
                }

                if (SendUsDBDiagramId == 0)
                {
                    return StatusCode(200, NewEnteredId); 
                }
                else
                {
                    return StatusCode(200, RowsAffected);
                }
            }
            catch (Exception ex) 
            { 
                DateTime Now = DateTime.Now;
                FailureModel FailureModel = new FailureModel()
                {
                    HTTPCode = 500,
                    Message = ex.Message,
                    EmergencyLevel = 1,
                    StackTrace = ex.StackTrace ?? "",
                    Source = ex.Source ?? "",
                    Comment = "",
                    Active = true,
                    UserCreationId = HttpContext.Session.GetInt32("UserId") ?? 1,
                    UserLastModificationId = HttpContext.Session.GetInt32("UserId") ?? 1,
                    DateTimeCreation = Now,
                    DateTimeLastModification = Now
                };
                FailureModel.Insert();
                return StatusCode(500, ex.Message);
            }
        }

