        [HttpPost("~/api/BasicCulture/City/1/InsertOrUpdateAsync")]
        public async Task<IActionResult> InsertOrUpdateAsync()
        {
            try
            {
                //Get UserId from Session
                int UserId = HttpContext.Session.GetInt32("UserId") ?? 0;

                if(UserId == 0)
                {
                    return StatusCode(401, "User not found in session");
                }
                
                #region Pass data from client to server
                //CityId
                int CityId = Convert.ToInt32(HttpContext.Request.Form["basicculture-city-cityid-input"]);
                
                string Name = HttpContext.Request.Form["basicculture-city-name-input"];
                string GeographicalCoordinates = HttpContext.Request.Form["basicculture-city-geographicalcoordinates-input"];
                string Code = HttpContext.Request.Form["basicculture-city-code-input"];
                int ProvinceId = 0; 
                if (Convert.ToInt32(HttpContext.Request.Form["basicculture-city-provinceid-input"]) != 0)
                {
                    ProvinceId = Convert.ToInt32(HttpContext.Request.Form["basicculture-city-provinceid-input"]);
                }
                else
                { return StatusCode(400, "It's not allowed to save zero values in ProvinceId"); }
                
                #endregion

                int NewEnteredId = 0;
                int RowsAffected = 0;

                if (CityId == 0)
                {
                    //Insert
                    CityModel CityModel = new CityModel()
                    {
                        Active = true,
                        UserCreationId = UserId,
                        UserLastModificationId = UserId,
                        DateTimeCreation = DateTime.Now,
                        DateTimeLastModification = DateTime.Now,
                        Name = Name,
                        GeographicalCoordinates = GeographicalCoordinates,
                        Code = Code,
                        ProvinceId = ProvinceId,
                        
                    };
                    
                    NewEnteredId = _ICity.Insert(CityModel);
                }
                else
                {
                    //Update
                    CityModel CityModel = new CityModel(CityId);
                    
                    CityModel.UserLastModificationId = UserId;
                    CityModel.DateTimeLastModification = DateTime.Now;
                    CityModel.Name = Name;
                    CityModel.GeographicalCoordinates = GeographicalCoordinates;
                    CityModel.Code = Code;
                    CityModel.ProvinceId = ProvinceId;
                                       

                    RowsAffected = _ICity.UpdateByCityId(CityModel);
                }
                

                //Look for sent files
                if (HttpContext.Request.Form.Files.Count != 0)
                {
                    int i = 0; //Used to iterate in HttpContext.Request.Form.Files
                    foreach (var File in Request.Form.Files)
                    {
                        if (File.Length > 0)
                        {
                            var FileName = HttpContext.Request.Form.Files[i].FileName;
                            var FilePath = $@"{_WebHostEnvironment.WebRootPath}/Uploads/BasicCulture/City/";

                            using (var FileStream = new FileStream($@"{FilePath}{FileName}", FileMode.Create))
                            {
                                
                                await File.CopyToAsync(FileStream); // Read file to stream
                                byte[] array = new byte[FileStream.Length]; // Stream to byte array
                                FileStream.Seek(0, SeekOrigin.Begin);
                                FileStream.Read(array, 0, array.Length);
                            }

                            i += 1;
                        }
                    }
                }

                if (CityId == 0)
                {
                    return StatusCode(200, NewEnteredId); 
                }
                else
                {
                    return StatusCode(200, RowsAffected);
                }
            }
            catch (Exception ex) 
            { 
                DateTime Now = DateTime.Now;
                FailureModel FailureModel = new FailureModel()
                {
                    HTTPCode = 500,
                    Message = ex.Message,
                    EmergencyLevel = 1,
                    StackTrace = ex.StackTrace ?? "",
                    Source = ex.Source ?? "",
                    Comment = "",
                    Active = true,
                    UserCreationId = HttpContext.Session.GetInt32("UserId") ?? 1,
                    UserLastModificationId = HttpContext.Session.GetInt32("UserId") ?? 1,
                    DateTimeCreation = Now,
                    DateTimeLastModification = Now
                };
                FailureModel.Insert();
                return StatusCode(500, ex.Message);
            }
        }

