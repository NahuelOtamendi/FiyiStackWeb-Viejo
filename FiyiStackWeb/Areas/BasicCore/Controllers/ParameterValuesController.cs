        [HttpPost("~/api/BasicCore/Parameter/1/InsertOrUpdateAsync")]
        public async Task<IActionResult> InsertOrUpdateAsync()
        {
            try
            {
                //Get UserId from Session
                int UserId = HttpContext.Session.GetInt32("UserId") ?? 0;

                if(UserId == 0)
                {
                    return StatusCode(401, "User not found in session");
                }
                
                #region Pass data from client to server
                //ParameterId
                int ParameterId = Convert.ToInt32(HttpContext.Request.Form["basiccore-parameter-parameterid-input"]);
                
                string Name = HttpContext.Request.Form["basiccore-parameter-name-input"];
                string Value = HttpContext.Request.Form["basiccore-parameter-value-input"];
                bool IsPrivate = Convert.ToBoolean(HttpContext.Request.Form["basiccore-parameter-isprivate-input"]);
                
                #endregion

                int NewEnteredId = 0;
                int RowsAffected = 0;

                if (ParameterId == 0)
                {
                    //Insert
                    ParameterModel ParameterModel = new ParameterModel()
                    {
                        Active = true,
                        UserCreationId = UserId,
                        UserLastModificationId = UserId,
                        DateTimeCreation = DateTime.Now,
                        DateTimeLastModification = DateTime.Now,
                        Name = Name,
                        Value = Value,
                        IsPrivate = IsPrivate,
                        
                    };
                    
                    NewEnteredId = _IParameter.Insert(ParameterModel);
                }
                else
                {
                    //Update
                    ParameterModel ParameterModel = new ParameterModel(ParameterId);
                    
                    ParameterModel.UserLastModificationId = UserId;
                    ParameterModel.DateTimeLastModification = DateTime.Now;
                    ParameterModel.Name = Name;
                    ParameterModel.Value = Value;
                    ParameterModel.IsPrivate = IsPrivate;
                                       

                    RowsAffected = _IParameter.UpdateByParameterId(ParameterModel);
                }
                

                //Look for sent files
                if (HttpContext.Request.Form.Files.Count != 0)
                {
                    int i = 0; //Used to iterate in HttpContext.Request.Form.Files
                    foreach (var File in Request.Form.Files)
                    {
                        if (File.Length > 0)
                        {
                            var FileName = HttpContext.Request.Form.Files[i].FileName;
                            var FilePath = $@"{_WebHostEnvironment.WebRootPath}/Uploads/BasicCore/Parameter/";

                            using (var FileStream = new FileStream($@"{FilePath}{FileName}", FileMode.Create))
                            {
                                
                                await File.CopyToAsync(FileStream); // Read file to stream
                                byte[] array = new byte[FileStream.Length]; // Stream to byte array
                                FileStream.Seek(0, SeekOrigin.Begin);
                                FileStream.Read(array, 0, array.Length);
                            }

                            i += 1;
                        }
                    }
                }

                if (ParameterId == 0)
                {
                    return StatusCode(200, NewEnteredId); 
                }
                else
                {
                    return StatusCode(200, RowsAffected);
                }
            }
            catch (Exception ex) 
            { 
                DateTime Now = DateTime.Now;
                FailureModel FailureModel = new FailureModel()
                {
                    HTTPCode = 500,
                    Message = ex.Message,
                    EmergencyLevel = 1,
                    StackTrace = ex.StackTrace ?? "",
                    Source = ex.Source ?? "",
                    Comment = "",
                    Active = true,
                    UserCreationId = HttpContext.Session.GetInt32("UserId") ?? 1,
                    UserLastModificationId = HttpContext.Session.GetInt32("UserId") ?? 1,
                    DateTimeCreation = Now,
                    DateTimeLastModification = Now
                };
                FailureModel.Insert();
                return StatusCode(500, ex.Message);
            }
        }

